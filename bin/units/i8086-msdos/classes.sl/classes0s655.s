SECTION .text
	ALIGN 2
	GLOBAL CLASSES_$$_INITCOMPONENTPAGES
CLASSES_$$_INITCOMPONENTPAGES:
		push	bp
		mov	bp,sp
		mov	ax,word VMT_$CLASSES_$$_TCOLLECTION
		push	ax
		mov	ax,1
		push	ax
		mov	ax,word VMT_$CLASSES_$$_TCOMPONENTPAGE
		push	ax
		call	CLASSES$_$TCOLLECTION_$__$$_CREATE$TCOLLECTIONITEMCLASS$$TCOLLECTION
		mov	word [U_$CLASSES_$$_COMPONENTPAGES],ax
		push	word [U_$CLASSES_$$_COMPONENTPAGES]
		call	CLASSES$_$TCOLLECTION_$__$$_ADD$$TCOLLECTIONITEM
		mov	sp,bp
		pop	bp
		ret
EXTERN	CLASSES$_$TCOLLECTION_$__$$_ADD$$TCOLLECTIONITEM
EXTERN	U_$CLASSES_$$_COMPONENTPAGES
EXTERN	CLASSES$_$TCOLLECTION_$__$$_CREATE$TCOLLECTIONITEMCLASS$$TCOLLECTION
EXTERN	VMT_$CLASSES_$$_TCOMPONENTPAGE
EXTERN	VMT_$CLASSES_$$_TCOLLECTION
